<h1>Chilltyping</h1>

<p>Do you like to just type? This is the right place for you! Pick from lots of short text snippets and just retype them. At the end check your stats to see how well you did. Along the way you even might learn something new.</p>
<h2>How to?</h2>
<p>Click either <a href='/type-random-text'>random text</a> and start typing, or <a href='/type-your-own-text'>own text</a> to retype a text of your own.</p>
